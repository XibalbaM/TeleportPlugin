package fr.xibalba.teleportplugin.models;

/**
 * JavaBean class representing a teleportation target.
 */
public class TeleportationTarget {
    private int x, y, z;
    private String world;

    public TeleportationTarget() {
    }

    public TeleportationTarget(int x, int y, int z, String world) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.world = world;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public String getWorld() {
        return world;
    }

    public void setWorld(String world) {
        this.world = world;
    }
}